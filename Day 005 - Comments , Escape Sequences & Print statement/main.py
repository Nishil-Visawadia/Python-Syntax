# Single line Comment

'''Multi line 
comment'''

print("Hello , how are you ! ") # this is also a comment


# \n is an escape sequence character, it is used to print the later part on new line
print("Hello , \n how are you ! ")

print("Hello \"How are you\" ! ") # Output: Hello "How are you" !
print("I\'m fine") # Output: I'm fine


# Print statements
print("Hey", 1, 2, sep="~")  # Output: Hey~1~2

print("Hey", 1, 2, sep="~", end="end\n")  # Output: Hey~1~2end (use \n so that the next print statement will be shown on next line)